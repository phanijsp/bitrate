package com.example.bitrate;

public class MOvie {

    private String name;
    private String value;

    public MOvie(String name, String value) {
        this.name = name;
        this.value = value;
    }



    public String getname() {
        return name;
    }

    public void setname(String name) {
        this.name = name;
    }

    public String getvalue() {
        return value;
    }

    public void setvalue(String value) {
        this.value = value;
    }
}